a = int(input("Введите число a: "))
b = int(input("Введите число b: "))

print("Сумма:", a + b)
print("Разность:", a - b)
print("Произведение:", a * b)

if b != 0:
    print("Частное:", a / b)
    print("Остаток от деления:", a % b)
else:
    print("Деление невозможно: делитель равен 0")

print("Степень:", a ** b)
